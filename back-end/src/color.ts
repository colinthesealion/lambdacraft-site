const multipliers = [180, 220, 255, 135];

export function getColor(id: number) {
  const baseIndex = Math.floor(id / 4);
  const shadeIndex = id % 4;
  if (baseIndex in baseColors) {
    const baseColor = baseColors[baseIndex];
    const multiplier = multipliers[shadeIndex];
    return [
      Math.floor((baseColor[0] * multiplier)/255),
      Math.floor((baseColor[1] * multiplier)/255),
      Math.floor((baseColor[2] * multiplier)/255),
      baseColor[3]
    ];
  }
  else {
    return baseColors[0];
  }
}